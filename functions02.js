// Exemplos de funções nativas do JavaScript

// Math

console.log(Math.round(4.3)); // Vai arrendondar um float p/ um numero inteiro

console.log(Math.ceil(4.3)); // Vai arrendodar para o maior inteiro

console.log(Math.floor(4.3)); // Vai arredondar para o menor inteiro

console.log(Math.trunc(4.3)); // Vai desconsiderar os decimais

console.log(Math.pow(4 , 2)); // Vai exponenciar 2 numeros (4 ao quadrado)

console.log(Math.sqrt(16)); // Square Root, raíz quadrada

console.log(Math.random()); // retorna um valor aleatório entre 0 e 1

